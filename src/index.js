export default function filterReactDomProps(props) {
  const filterProps = {};

  for (const prop in props) {
    if (props.hasOwnProperty(prop) && DOMProps.indexOf(prop) !== -1) {
      filterProps[prop] = props[prop];
    }
  }
  return filterProps;
}
